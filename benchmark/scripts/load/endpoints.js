import http from 'k6/http';
import { check, group, sleep } from 'k6';
import { Trend, Rate } from 'k6/metrics';
import { loginSession, authHeaders } from './k6-auth.js';

const config = JSON.parse(open('../../config/endpoints.json'));

let manifest;
try {
  manifest = JSON.parse(open('../../results/seed/manifest.json'));
} catch {
  manifest = {
    benchmarkUsers: { superadmin: 1, admin: 2, employee: 3, client: 4 },
    sampleServiceId: 1,
    sampleOrderIds: [1],
    credentials: config.credentials,
  };
}

const BASE_URL = __ENV.BASE_URL || config.baseUrl;
const readableEndpoints = config.endpoints.filter((ep) => !ep.skipLoad);
const errorRate = new Rate('endpoint_errors');
const endpointTrends = Object.fromEntries(
  readableEndpoints.map((ep) => [
    ep.id,
    new Trend(`endpoint_${ep.id.replace(/-/g, '_')}`),
  ])
);

function resolvePath(path) {
  const m = manifest;
  return path
    .replace('{{admin.userId}}', m.benchmarkUsers.admin)
    .replace('{{employee.userId}}', m.benchmarkUsers.employee)
    .replace('{{client.userId}}', m.benchmarkUsers.client)
    .replace('{{service.id}}', m.sampleServiceId)
    .replace('{{order.id}}', m.sampleOrderIds[0])
    .replace('{{payment.reference}}', 'PAY-BENCH-REF');
}

function resolveBody(body, creds) {
  if (!body) return null;
  return JSON.stringify(body)
    .replace('{{admin.username}}', creds.admin.username)
    .replace('{{admin.password}}', creds.admin.password);
}

function getAuth(ep, tokens) {
  const role = ep.auth;
  if (role === 'none' || role === 'cookie') return {};
  const session = tokens[role];
  if (!session) return {};
  const headers = authHeaders(session);
  if (ep.csrf && session.csrf) {
    headers['X-CSRF-Token'] = session.csrf;
  }
  return headers;
}

export function setup() {
  const creds = manifest.credentials || config.credentials;
  const tokens = {};
  for (const role of ['superadmin', 'admin', 'employee', 'client']) {
    const c = creds[role];
    tokens[role] = loginSession(BASE_URL, c.username, c.password);
  }
  return { tokens, creds };
}

export const options = {
  setupTimeout: '3m',
  scenarios: {
    endpoints: {
      executor: 'per-vu-iterations',
      vus: 1,
      iterations: 1,
      maxDuration: '30m',
    },
  },
  thresholds: {
    endpoint_errors: ['rate<0.5'],
  },
};

export default function (data) {
  const { tokens, creds } = data;

  for (const ep of readableEndpoints) {
    group(ep.id, () => {
      const path = resolvePath(ep.path);
      let url = `${BASE_URL}${path}`;
      if (ep.query) {
        const qs = Object.entries(ep.query)
          .map(([k, v]) => `${k}=${encodeURIComponent(v)}`)
          .join('&');
        url += `?${qs}`;
      }

      const headers = {
        'Content-Type': 'application/json',
        ...getAuth(ep, tokens),
      };

      const body = resolveBody(ep.body, creds);
      const params = { headers, tags: { endpoint: ep.id } };
      let res;

      switch (ep.method) {
        case 'GET':
          res = http.get(url, params);
          break;
        case 'POST':
          res = http.post(url, body || '{}', params);
          break;
        case 'PUT':
          res = http.put(url, body || '{}', params);
          break;
        case 'PATCH':
          res = http.patch(url, body || '{}', params);
          break;
        default:
          res = http.get(url, params);
      }

      const ok = check(res, {
        'status 2xx or 4xx expected': (r) => r.status >= 200 && r.status < 500,
      });
      errorRate.add(!ok);
      endpointTrends[ep.id].add(res.timings.duration);

      if (ep.critical) {
        check(res, { 'critical not 5xx': (r) => r.status < 500 });
      }
    });
    sleep(0.1);
  }
}

export function handleSummary(data) {
  const lines = ['# Endpoint Benchmark Summary', '', 'Generated by k6', ''];
  const metrics = data.metrics || {};

  lines.push('| Metric | Value |');
  lines.push('|--------|-------|');
  if (metrics.http_req_duration) {
    lines.push(`| Avg latency | ${metrics.http_req_duration.values.avg?.toFixed(2)} ms |`);
    lines.push(`| P95 latency | ${metrics.http_req_duration.values['p(95)']?.toFixed(2)} ms |`);
    lines.push(`| P99 latency | ${metrics.http_req_duration.values['p(99)']?.toFixed(2)} ms |`);
  }
  if (metrics.http_reqs) {
    lines.push(`| Requests | ${metrics.http_reqs.values.count} |`);
    lines.push(`| Req/sec | ${metrics.http_reqs.values.rate?.toFixed(2)} |`);
  }

  return {
    'benchmark/results/load/summary.json': JSON.stringify(data, null, 2),
    'benchmark/results/load/summary.md': lines.join('\n'),
    stdout: lines.join('\n'),
  };
}
